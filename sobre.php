<script>
function toggleTooltip(el) {
  el.classList.toggle("active");
}

document.addEventListener("click", function (e) {
  const tooltips = document.querySelectorAll(".tooltip-termo");

  tooltips.forEach(t => {
    if (!t.contains(e.target)) {
      t.classList.remove("active");
    }
  });
});
